public class ch4_3 {
    public static void main(String[] args) {
        int a=5, b=5;

        System.out.printf("++a return value :%d\n", ++a);
        System.out.printf("After executing ++a, a = %d\n", a);
        System.out.printf("b++ return value :%d\n", b++);
        System.out.printf("After executing b++, b = %d\n");
    }
}
